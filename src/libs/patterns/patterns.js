// 正则验证规则
export default {
  // 手机号
  phone: /^1[34578]\d{9}$/,
  // 座机
  telephone: /^([0-9]{3,4}-)?[0-9]{7,8}$/,
  // 正整数
  positiveInteger: /^[1-9]\d*$/,
  // 大于1的正整数
  positiveInteger2: /^[2-9]\d*$/,
  // 百分比不超过两位小数
  percentage: /^[1-9]{0,1}\d{1}((\.[1-9]{1,2})|(\.0[1-9]){1,2})?$/,
  // 身份证号码
  idCode: /^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$/,
  // 中文
  cn: /^[\u4e00-\u9fa5]+$/,
  // 电子邮箱
  email: /^[a-zA-Z\d]+([-_\.][a-zA-Z\d]+)*@[a-zA-Z\d]+\.[a-zA-Z\d]{2,4}$/,
  // 网址
  url: /^((https|http|ftp|rtsp|mms)?:\/\/)[^\s]+/,
  // ip
  ip: /((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})(\.((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})){3}/,
  // 数字
  number: /^[0-9]*$/,
  // 正浮点数
  float: /^(0|[1-9]\d*)(\.\d+)?$/,
  // 非负整数
  NNI: /^\d+$/,
  // 非正整数
  NPI: /^-[1-9]\d*|0$/,
  // 新规则名称
  patternName: /xxx/,
}
